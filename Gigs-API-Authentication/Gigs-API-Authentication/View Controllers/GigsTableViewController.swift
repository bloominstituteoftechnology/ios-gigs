import UIKit

class GigsTableViewController: UITableViewController {

    //MARK: Properties
    var gigController = GigController() // property with a new instance of GigController
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }

    override func viewDidAppear(_ animated: Bool) { // Call viewDidAppear
        super.viewDidAppear(animated)
        // Check if GigController property's bearer is nil, if it is perform segue to login
            if gigController.bearer == nil {
                performSegue(withIdentifier: "LoginViewModalSegue", sender: self)
            }
        }

    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return gigController.gigs.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "GigsTableViewCell", for: indexPath)
        return cell
    }
    

